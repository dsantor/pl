package com.dakiplast.requests;

public class DoorRequest {

	private String sort;
	private String type;
	private String openSide;
	private String glass;
	private Long width;
	private Long height;
	private Long innerWidth;
	private Long price;
	private Long quantity;
	private Long id;
	
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getOpenSide() {
		return openSide;
	}
	public void setOpenSide(String openSide) {
		this.openSide = openSide;
	}
	public String getGlass() {
		return glass;
	}
	public void setGlass(String glass) {
		this.glass = glass;
	}
	public Long getWidth() {
		return width;
	}
	public void setWidth(Long width) {
		this.width = width;
	}
	public Long getHeight() {
		return height;
	}
	public void setHeight(Long height) {
		this.height = height;
	}
	public Long getInnerWidth() {
		return innerWidth;
	}
	public void setInnerWidth(Long innerWidth) {
		this.innerWidth = innerWidth;
	}
	public Long getQuantity() {
		return quantity;
	}
	public void setQuantity(Long quantity) {
		this.quantity = quantity;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getPrice() {
		return price;
	}
	public void setPrice(Long price) {
		this.price = price;
	}
}
